package string2;

public class StartWith {
	public static void main(String[] args) {
		String str = "trilok programming";
		System.out.println(str.startsWith("trilok"));
	}
}

class EndWith {
	public static void main(String[] args) {
		String str = "code with java";
		System.out.println(str.endsWith("python"));
	}
}